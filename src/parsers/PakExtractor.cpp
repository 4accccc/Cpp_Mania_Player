bool PakExtractor::loadKeys(const std::string& keyDir) {
    // Use embedded keys instead of loading from files
    key1a_.assign(EMBEDDED_KEY1A, EMBEDDED_KEY1A + sizeof(EMBEDDED_KEY1A));
    key1b_.assign(EMBEDDED_KEY1B, EMBEDDED_KEY1B + sizeof(EMBEDDED_KEY1B));

    keysLoaded_ = true;
    std::cout << "Loaded embedded PAK keys: key1a=" << key1a_.size()
              << " bytes, key1b=" << key1b_.size() << " bytes" << std::endl;
    return true;
}

// RSA helper: (inputB * input_) % key with modular multiplication
static uint64_t rsaMul(uint64_t inputB, uint64_t input_, uint64_t key) {
    uint64_t output = 0;
    while (input_ != 0) {
        if (input_ % 2 == 1) {
            output = (inputB + output) % key;
        }
        inputB = (inputB * 2) % key;
        input_ /= 2;
    }
    return output;
}

// RSA decrypt 8 bytes to 4 bytes
uint32_t PakExtractor::rsaDecrypt8to4(uint64_t input, uint64_t key1, uint64_t key2) {
    uint64_t output = 1;
    while (key2 != 0) {
        if (key2 % 2 == 1) {
            output = rsaMul(output, input, key1);
        }
        input = rsaMul(input, input, key1);
        key2 /= 2;
    }
    return static_cast<uint32_t>(output);
}

std::vector<uint8_t> PakExtractor::xipRsaDecrypt(const uint8_t* data, size_t size, uint8_t keyIndex) {
    std::vector<uint8_t> output;
    if (size % 8 != 0) return output;

    size_t count = size / 8;
    for (size_t i = 0; i < count; i++) {
        uint64_t inputVal;
        memcpy(&inputVal, data + i * 8, 8);

        uint64_t k1, k2;
        memcpy(&k1, key1a_.data() + keyIndex * 8, 8);
        memcpy(&k2, key1b_.data() + keyIndex * 8, 8);

        uint32_t result = rsaDecrypt8to4(inputVal, k1, k2);

        uint8_t bytes[4];
        memcpy(bytes, &result, 4);
        output.insert(output.end(), bytes, bytes + 4);

        keyIndex = (keyIndex + 1) % 256;
    }
    return output;
}

std::vector<uint8_t> PakExtractor::japUnXor(const uint8_t* data, size_t size, int keyOffset) {
    std::vector<uint8_t> output(size);
    keyOffset &= 0xFF;

    for (size_t i = 0; i < size; i++) {
        output[i] = getJapKeyByte(keyOffset) ^ data[i];
        keyOffset = (keyOffset + 1) & 0xFF;
    }
    return output;
}

bool PakExtractor::parseFileHeader(const uint8_t* data, size_t offset, int xorParam, PakFileEntry& entry) {
    auto header = japUnXor(data + offset, FILE_HEADER_LENGTH, xorParam);

    memcpy(&entry.fileBlockSize, header.data(), 4);
    memcpy(&entry.uncompressedSize, header.data() + 4, 4);

    char filename[0x105] = {0};
    memcpy(filename, header.data() + 0x0c, FILE_NAME_LENGTH);
    entry.filename = filename;
    std::replace(entry.filename.begin(), entry.filename.end(), '\\', '/');

    memcpy(&entry.crc32, header.data() + 0x110, 4);
    entry.cryptKeyIndex = header[0x118];
    entry.dataOffset = offset + FILE_HEADER_LENGTH;

    return true;
}

bool PakExtractor::open(const std::string& pakPath) {
    if (!keysLoaded_) {
        std::cerr << "Keys not loaded" << std::endl;
        return false;
    }

    close();

    std::ifstream file(pakPath, std::ios::binary);
    if (!file.is_open()) {
        std::cerr << "Failed to open pak file: " << pakPath << std::endl;
        return false;
    }

    file.seekg(0, std::ios::end);
    size_t fileSize = file.tellg();
    file.seekg(0, std::ios::beg);

    pakData_.resize(fileSize);
    file.read(reinterpret_cast<char*>(pakData_.data()), fileSize);
    file.close();

    // Verify magic "XIP2"
    if (fileSize < 12 || pakData_[0] != 'X' || pakData_[1] != 'I' ||
        pakData_[2] != 'P' || pakData_[3] != '2') {
        std::cerr << "Not a valid XIP2 file" << std::endl;
        return false;
    }

    // Parse secretA offset
    uint32_t offsetSecretA = pakData_[4] | (pakData_[6] << 8) |
                             (pakData_[8] << 16) | (pakData_[11] << 24);
    uint16_t secretASkipSize = pakData_[9] | (pakData_[7] << 8);

    if (offsetSecretA + 24 > fileSize) return false;

    // Decrypt secretA
    auto secretA = xipRsaDecrypt(pakData_.data() + offsetSecretA, 24, 0x0c);
    if (secretA.size() < 12) return false;

    uint32_t fileOffset, numberOfFiles;
    memcpy(&fileOffset, secretA.data() + 2, 4);
    memcpy(&numberOfFiles, secretA.data() + 6, 4);

    std::cout << "secretA decrypted: fileOffset=" << fileOffset
              << ", numberOfFiles=" << numberOfFiles << std::endl;

    if (fileOffset > 0x3000 || numberOfFiles > 10000) {
        std::cerr << "Invalid pak header" << std::endl;
        return false;
    }

    // Parse file entries
    for (uint32_t i = 0; i < numberOfFiles; i++) {
        std::cout << "Parsing file " << i << "/" << numberOfFiles << " at offset " << fileOffset << std::endl;
        if (fileOffset == offsetSecretA) {
            fileOffset += secretASkipSize;
        }
        if (fileOffset + FILE_HEADER_LENGTH > fileSize) {
            std::cout << "Offset out of bounds, breaking" << std::endl;
            break;
        }

        PakFileEntry entry;
        int xorParam = numberOfFiles - i;
        if (!parseFileHeader(pakData_.data(), fileOffset, xorParam, entry)) {
            break;
        }

        // Parse size fields for extraction
        if (entry.dataOffset + 8 <= fileSize) {
            uint32_t s1, s2;
            memcpy(&s1, pakData_.data() + entry.dataOffset, 4);
            memcpy(&s2, pakData_.data() + entry.dataOffset + 4, 4);

            entry.cryptChunkSize = (((s2 >> 8 & 0xff) << 8 | s1 & 0xff) << 8 |
                                    s1 >> 24) << 8 | (s1 >> 8 & 0xff);
            entry.rsaDecryptedSize = (((s1 >> 16 & 0xff) << 8 | s2 >> 24) << 8 |
                                      s2 & 0xff) << 8 | (s2 >> 16 & 0xff);
        }

        files_.push_back(entry);
        std::string lowerName = entry.filename;
        std::transform(lowerName.begin(), lowerName.end(), lowerName.begin(), ::tolower);
        fileIndex_[lowerName] = files_.size() - 1;

        fileOffset += FILE_HEADER_LENGTH + entry.fileBlockSize;
    }

    isOpen_ = true;
    currentPakPath_ = pakPath;
    std::cout << "Opened pak: " << pakPath << " (" << files_.size() << " files)" << std::endl;
    return true;
}

void PakExtractor::close() {
    pakData_.clear();
    files_.clear();
    fileIndex_.clear();
    isOpen_ = false;
    currentPakPath_.clear();
}

bool PakExtractor::hasFile(const std::string& filename) const {
    std::string lower = filename;
    std::transform(lower.begin(), lower.end(), lower.begin(), ::tolower);
    return fileIndex_.find(lower) != fileIndex_.end();
}

bool PakExtractor::extractEntry(const PakFileEntry& entry, std::vector<uint8_t>& outData) {
    size_t dataStart = entry.dataOffset + 8;
    size_t encSize = entry.cryptChunkSize;
    size_t rsaSize = entry.rsaDecryptedSize;

    if (dataStart + encSize > pakData_.size()) return false;

    // RSA decrypt the encrypted chunk
    auto decrypted = xipRsaDecrypt(pakData_.data() + dataStart, rsaSize * 2, entry.cryptKeyIndex);

    // Build compressed data: decrypted + rest
    std::vector<uint8_t> compressed;
    compressed.insert(compressed.end(), decrypted.begin(), decrypted.end());

    size_t restOffset = dataStart + encSize;
    size_t restSize = entry.fileBlockSize - 8 - encSize;
    if (restOffset + restSize <= pakData_.size()) {
        compressed.insert(compressed.end(),
                          pakData_.data() + restOffset,
                          pakData_.data() + restOffset + restSize);
    }

    // LZO decompress
    outData.resize(entry.uncompressedSize + 256);
    lzo_uint outLen = outData.size();

    int r = lzo1x_decompress(compressed.data(), compressed.size(),
                             outData.data(), &outLen, nullptr);
    if (r != LZO_E_OK) {
        std::cerr << "LZO decompress failed: " << r << std::endl;
        return false;
    }

    outData.resize(outLen);
    return true;
}

bool PakExtractor::extractFile(const std::string& filename, std::vector<uint8_t>& outData) {
    if (!isOpen_) return false;

    std::string lower = filename;
    std::transform(lower.begin(), lower.end(), lower.begin(), ::tolower);

    auto it = fileIndex_.find(lower);
    if (it == fileIndex_.end()) return false;

    return extractEntry(files_[it->second], outData);
}

bool PakExtractor::extractAll(const std::string& outDir) {
    if (!isOpen_) return false;

    std::filesystem::create_directories(outDir);

    int success = 0;
    for (const auto& entry : files_) {
        std::vector<uint8_t> data;
        if (extractEntry(entry, data)) {
            std::filesystem::path outPath = std::filesystem::path(outDir) / entry.filename;
            std::filesystem::create_directories(outPath.parent_path());

            std::ofstream out(outPath, std::ios::binary);
            if (out.is_open()) {
                out.write(reinterpret_cast<char*>(data.data()), data.size());
                out.close();
                success++;
            }
        }
    }

    std::cout << "Extracted " << success << "/" << files_.size() << " files" << std::endl;
    return success > 0;
}
